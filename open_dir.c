#include "incl/ls.h"

DIR			*open_dir(char **dir)
{
	DIR		*ans;

	if (!(ans = opendir(*dir)))
	{
		error_errno(*dir);
		ft_strdel(dir);
		return (0);
	}
	return (ans);
}
